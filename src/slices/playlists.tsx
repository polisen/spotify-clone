interface Track {
  trackName: string;
  mediaLink: string;
}

interface AlbumInfo {
  artist: string;
  album: string;
  coverArt: string;
  tracks: Track[];
}

const takeFromAlbum = (arr: number[], album: AlbumInfo): Array<object> => {
  const resultArray: any = [];
  arr.forEach((idx) => {
    resultArray.push({
      album: album.album,
      artist: album.artist,
      coverArt: album.coverArt,
      ...album.tracks[idx],
    });
  });
  return resultArray;
};

const getEven = (num: number): number => {
  if (num === 1) return num;
  if (num % 4 === 0) return num;
  return 1;
};

const getCoverArt = function getPlaylistCoverArtAssortment(arr: any): string[] {
  const uniqueCovers = [
    ...new Set(arr.map(([, album]: any) => album.coverArt)),
  ];
  const randomSelection: any = uniqueCovers
    .sort(() => 0.5 - Math.random())
    .slice(0, uniqueCovers.length > 4 ? 4 : getEven(uniqueCovers.length));
  return randomSelection;
};

